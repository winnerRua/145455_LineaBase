using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.ComponentModel.DataAnnotations;

namespace WAREGBOGWMM.Models
{
    public class Rol
    {
        [Display(Name = "Nombre de rol")]
        [Required(ErrorMessage = "Descripción de rol es requerido.")]
        public string nombreRol { get; set; }

        [Display(Name = "Estado de rol")]
        [Required(ErrorMessage = "Estado de rol es requerido.")]
        public string estadoActivo { get; set; }

        [Display(Name = "Código de rol")]
        [Required(ErrorMessage = "Código rol es requerido.")]
        public string rolID { get; set; }
    }
}